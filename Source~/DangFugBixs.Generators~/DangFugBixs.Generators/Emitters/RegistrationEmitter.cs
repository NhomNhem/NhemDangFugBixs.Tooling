using System.CodeDom.Compiler;
using System.Collections.Generic;
using System.Text;
using System.Linq;
using NhemDangFugBixs.Common.Models;
using NhemDangFugBixs.Generators.Utils;

namespace NhemDangFugBixs.Generators.Emitters;

internal static class RegistrationEmitter {
    public static string GenerateSource(
        IEnumerable<ServiceInfo> services,
        IEnumerable<SceneInjectionInfo> sceneServices,
        string assemblyName) {

        string sanitizedAssembly = assemblyName.Replace(".", "").Replace("-", "");

        using var stringWriter = new StringWriter();
        using var writer = new IndentedTextWriter(stringWriter, "    ");

        // 1. Header
        writer.WriteLine("// <auto-generated />");
        writer.WriteLine("// Generated by NhemDangFugBixs.Tooling v3.0");
        writer.WriteLine("// Type-safe scope references with convention-based naming");
        writer.WriteLine();
        writer.WriteLine("using System;");
        writer.WriteLine("using VContainer;");
        writer.WriteLine("using VContainer.Unity;");
        writer.WriteLine("#if UNITY_5_3_OR_NEWER");
        writer.WriteLine("using UnityEngine;");
        writer.WriteLine("#endif");
        writer.WriteLine();

        using (writer.Block($"namespace NhemDangFugBixs.Generated.{sanitizedAssembly}")) {

            // 1. Blueprint for Editor Discovery (Scene Injection)
            using (writer.Block("public static class SceneInjectionBlueprint")) {
                writer.WriteLine("public static readonly Type[] ComponentTypes = {");
                writer.Indent++;
                foreach (var svc in sceneServices) {
                    writer.WriteLine($"typeof({svc.FullName}),");
                }
                writer.Indent--;
                writer.WriteLine("};");
            }
            writer.WriteLine();

            // 2. Standard VContainer Registrations
            using (writer.Block("public static class VContainerRegistration")) {

                // Group services by their scope type name (for type-safe scopes) or scope name (legacy)
                var groups = services.GroupBy(s => GetScopeKey(s)).ToDictionary(g => g.Key, g => g.ToList());

                foreach (var group in groups) {
                    string methodName = GetRegistrationMethodName(group.Key);

                    using (writer.Block($"public static void {methodName}(IContainerBuilder builder)")) {
                        writer.WriteLine("VContainer.Unity.EntryPointsBuilder.EnsureDispatcherRegistered(builder);");

                        // Emit service registrations
                        foreach (var svc in group.Value) {
                            var suffix = "";

                            if (svc.AsTypes != null && svc.AsTypes.Length > 0) {
                                suffix += $".As({string.Join(", ", svc.AsTypes.Select(t => $"typeof({t})"))})";
                            } else {
                                // If it's an entry point, AsImplementedInterfaces is already handled by RegisterEntryPoint
                                if (svc.AsImplementedInterfaces && !svc.IsEntryPoint) suffix += ".AsImplementedInterfaces()";
                                if (svc.AsSelf) suffix += ".AsSelf()";
                            }

                            if (svc.RegisterInHierarchy) {
                                writer.WriteLine($"builder.RegisterComponentInHierarchy<{svc.FullName}>(){suffix};");
                            } else if (svc.IsComponent) {
                                writer.WriteLine($"builder.RegisterComponentOnNewGameObject<{svc.FullName}>(Lifetime.{svc.Lifetime}){suffix};");
                            } else if (svc.IsFactory) {
                                writer.WriteLine($"builder.RegisterFactory<{svc.FullName}>(c => () => c.Resolve<{svc.FullName}>(), Lifetime.{svc.Lifetime}){suffix};");
                            } else if (svc.IsEntryPoint) {
                                writer.WriteLine($"builder.RegisterEntryPoint<{svc.FullName}>(Lifetime.{svc.Lifetime}){suffix};");
                            } else {
                                writer.WriteLine($"builder.Register<{svc.FullName}>(Lifetime.{svc.Lifetime}){suffix};");
                            }
                        }
                    }
                    writer.WriteLine();
                }
            }
        }
        return stringWriter.ToString();
    }

    /// <summary>
    /// Gets the grouping key for a service - either the scope type name (for type-safe) or scope name (legacy).
    /// </summary>
    private static string GetScopeKey(ServiceInfo service) {
        // For type-safe scopes, use the scope type name
        if (service.UsesTypeSafeScope && !string.IsNullOrEmpty(service.ScopeTypeName)) {
            return service.ScopeTypeName;
        }
        // Fallback to legacy scope name
        return service.ScopeName;
    }

    /// <summary>
    /// Generates the registration method name from a scope key.
    /// For type-safe scopes: strips "LifetimeScope" suffix (e.g., GameplayLifetimeScope → RegisterGameplay)
    /// For legacy scopes: uses the scope name directly (e.g., "Gameplay" → RegisterGameplay)
    /// </summary>
    private static string GetRegistrationMethodName(string scopeKey) {
        // Check if this looks like a type name (ends with LifetimeScope)
        if (scopeKey.EndsWith("LifetimeScope", StringComparison.Ordinal)) {
            // Strip "LifetimeScope" suffix
            string name = scopeKey.Substring(0, scopeKey.Length - "LifetimeScope".Length);
            // Handle edge case: if nothing left, use original
            if (string.IsNullOrEmpty(name)) {
                return "RegisterGlobal";
            }
            return $"Register{name}";
        }
        
        // Legacy scope name or custom name - use as-is
        return $"Register{scopeKey}";
    }
}
